function buscarLivro() {
    var dia = document.getElementById("Dia").value;
    var livro_encontrado = null;
    debugger

    for (let posicao = 0; posicao < lista_biblia.length; posicao = posicao + 1) {
        debugger
        if (dia == lista_biblia[posicao].dia) {
            livro_encontrado = lista_biblia[posicao];
            break;
        }

        if (dia == "0606") {
            break
        }

        if (dia == "1606") {
            break
        }

    }

    var lista_ul = document.getElementById("lista_biblia");
    lista_ul.innerHTML = ""
    if (livro_encontrado != null) {

        var dia = ""

        for (let posicao = 0; posicao < livro_encontrado.versos.length; posicao = posicao + 1) {
            dia = dia + `
        <p>
            <h4>${livro_encontrado.versos[posicao].Titulo}</h4>
            <span>${livro_encontrado.versos[posicao].texto}</span>
        </p>
    `
        }

        lista_ul.innerHTML = dia
    }


    else {
        lista_ul.innerText = "Oração não encontrada!"
    }

    if (dia == "0606") {
        AudioPrincipal.src = Audio3.src;
        lista_ul.innerText = "Bom Metal Nobre!"
        textoCima.innerText = "Louvai"
    }

}

